    def __init__(self):
        self._holidays_dates = set(from_datenum(dn)
                                   for dn in self._DATENUM_HOLIDAYS)

    def is_holiday(self, d):
        """Return True if the date is a TARGET holiday."""
        if hasattr(d, 'date'):
            d = d.date()
        return d in self._holidays_dates

    def is_business_day(self, d):
        """Return True if the date is a TARGET business day (not weekend, not holiday)."""
        if hasattr(d, 'date'):
            d = d.date()
        return d.weekday() < 5 and not self.is_holiday(d)

    def busdate(self, d, direction='follow'):
        """
        Adjust a date to the nearest TARGET business day.

        Args:
            d: date to adjust.
            direction: 'follow' moves forward, 'previous' moves backward.

        Returns:
            The nearest business day in the requested direction.
        """
        if hasattr(d, 'date'):
            d = d.date()
        step = timedelta(
            days=1) if direction == 'follow' else timedelta(days=-1)
        while not self.is_business_day(d):
            d = d + step
        return d
